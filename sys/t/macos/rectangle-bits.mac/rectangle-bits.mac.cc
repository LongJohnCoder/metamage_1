static inline int rowBytes_from_bitwidth( int bits )
{
	return (bits + 15) / 16 * 2;
}

const int icon_width  = 32;
const int icon_height = 32;

const int icon_px = icon_width * icon_height;  // 1024
const int icon_bytes = icon_px / 8;  // 128;

const int icon_rowBytes = rowBytes_from_bitwidth( icon_width );  // 4

QDGlobals qd;

static GrafPtr testing_grafPort;

static void init()
{
	InitGraf( &qd.thePort );
	
	testing_grafPort = (GrafPtr) malloc( sizeof (GrafPort) );
	
	OpenPort( testing_grafPort );
	
	PortSize( 32, 32 );
	
	char* mem = (char*) malloc( icon_bytes );
	
	BitMap bitmap = { mem, icon_rowBytes, { 0, 0, icon_height, icon_width } };
	
	SetPortBits( &bitmap );
}

static void term()
{
	SetPort( NULL );
	
	if ( testing_grafPort != NULL )
	{
		free( testing_grafPort->portBits.baseAddr );
		
		ClosePort( testing_grafPort );
		
		free( testing_grafPort );
	}
}

static bool mem_only_contains( const char* p, const char* end, char c )
{
	while ( p < end )
	{
		if ( *p++ != c )
		{
			return false;
		}
	}
	
	return true;
}

static void basics( const char* base, const Rect& bounds )
{
	const char* end = base + icon_bytes;
	
	PaintRect( &bounds );
	
	EXPECT( mem_only_contains( base, end, 0xFF ) );
	
	
	Pattern vstripes = { 0xAA, 0xAA, 0xAA, 0xAA, 0xAA, 0xAA, 0xAA, 0xAA };
	
	FillRect( &bounds, &vstripes );
	
	EXPECT( mem_only_contains( base, end, 0xAA ) );
	
	
	InvertRect( &bounds );
	
	EXPECT( mem_only_contains( base, end, 0x55 ) );
	
	
	EraseRect( &bounds );
	
	EXPECT( mem_only_contains( base, end, 0x00 ) );
	
	
	FillRect( &bounds, &qd.black );
	
	EXPECT( mem_only_contains( base, end, 0xFF ) );
	
	
	FillRect( &bounds, &qd.dkGray );
	
	EXPECT_ICONS( base, dkGray_32 );
	
	
	FillRect( &bounds, &qd.gray );
	
	EXPECT_ICONS( base, gray_32 );
	
	
	FillRect( &bounds, &qd.ltGray );
	
	EXPECT_ICONS( base, ltGray_32 );
	
	
	FillRect( &bounds, &qd.white );
	
	EXPECT( mem_only_contains( base, end, 0x00 ) );
	
	
	FrameRect( &bounds );
	
	EXPECT_ICONS( base, square_32 );
}

static void frames( const char* base, const Rect& bounds )
{
	Rect rect = bounds;
	
	InsetRect( &rect, 2, 2 );
	
	FrameRect( &rect );
	
	EXPECT_ICONS( base, square_32_30 );
	
	
	OffsetRect( &rect, 5, 1 );
	
	FrameRect( &rect );
	
	EXPECT_ICONS( base, square_32_30_off1 );
	
	
	OffsetRect( &rect, 20, 22 );
	
	FrameRect( &rect );
	
	EXPECT_ICONS( base, square_32_30_off2 );
	
	rect.bottom = 4;
	rect.right  = 4;
	
	FrameRect( &rect );  // empty rect, does nothing
	
	EXPECT_ICONS( base, square_32_30_off2 );
	
	
	rect.top  = 4;
	rect.left = 4;
	
	FrameRect( &rect );  // empty rect, does nothing
	
	EXPECT_ICONS( base, square_32_30_off2 );
	
	
	rect.top  = -32767;
	rect.left = -32767;
	
	FrameRect( &rect );
	
	EXPECT_ICONS( base, square_32_30_off3 );
}

static void offset( const char* base, const Rect& bounds )
{
	Rect rect = bounds;
	
	MovePortTo( 1, 1 );
	
	Rect temp = rect;
	
	OffsetRect( &temp, -1, -1 );
	
	EXPECT_RECTS( temp, bounds );
	EXPECT_RECTS( rect, qd.thePort->portRect );
	
	rect.right  -= 2;
	rect.bottom -= 2;
	
	PortSize( 1, 1 );  // do we *really* clip to the portRect?  (no)
	
	EraseRect( &rect );
	
	EXPECT_ICONS( base, square_32 );
	
	MovePortTo( 7, 5 );
	
	InsetRect( &rect, 2, 2 );
	
	FrameRect( &rect );
	
	EXPECT_ICONS( base, square_32_offset_inset );
	
	PortSize( 32, 32 );
	
	MovePortTo( 0, 0 );
}

static void fills( const char* base, const Rect& bounds )
{
	Rect rect = bounds;
	
	InsetRect( &rect, 1, 1 );
	
	const Pattern vstripe = { 0x80, 0x80, 0x80, 0x80, 0x80, 0x80, 0x80, 0x80 };
	const Pattern hstripe = { 0xFF, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00 };
	const Pattern one_dot = { 0x80, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00 };
	
	FillRect( &rect, &vstripe );
	
	EXPECT_ICONS( base, vstripe_32_framed );
	
	FillRect( &rect, &hstripe );
	
	EXPECT_ICONS( base, hstripe_32_framed );
	
	FillRect( &rect, &one_dot );
	
	EXPECT_ICONS( base, one_dot_32_framed );
	
	
	FillRect( &rect, &qd.gray );
	
	EXPECT_ICONS( base, gray_32_framed );
}


int main( int argc, char** argv )
{
	tap::start( "rectangle-bits.mac", n_tests );
	
	init();
	
	const char* base   = testing_grafPort->portBits.baseAddr;
	const Rect& bounds = testing_grafPort->portBits.bounds;
	
	basics( base, bounds );
	frames( base, bounds );
	offset( base, bounds );
	fills ( base, bounds );
	
	term();
	
	return 0;
}
